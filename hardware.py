"""
Cat-qubit power, cabling, and noise utilities.

This module provides:
- Cryostat cabling models (attenuation, thermal conductance, passive heat loads)
- Hardware parameter container with derived coefficients
- Drive/interaction strength helpers and gate-time formulas
- Power accounting for pump/drive/readout
- Stim circuits for a repetition code and a noise injection pass

All temperatures are in Kelvin, frequencies in angular units (rad/s),
and powers/energies follow the conventions in the associated report/notes.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Callable, Dict, Iterable, List, Literal, Tuple, Optional, Any

import numpy as np
import scipy.optimize as scopt
from numpy.typing import NDArray
from scipy.integrate import quad
from scipy.constants import hbar, Boltzmann

from cables import cables  # cable database generated by William

# ---------------------------------------------------------------------
# Constants & Units
# ---------------------------------------------------------------------

twopi: float = 2 * np.pi
Hz, kHz, MHz, GHz = twopi, twopi * 1e3, twopi * 1e6, twopi * 1e9
us, ns = 1e-6, 1e-9
pH = 1e-12  # PicoHenry

HBAR: float = hbar
KB: float = Boltzmann

# Obélix temperatures on 03/12/2024 @ 11:08 (cold → warm)
STAGE_TEMPS: NDArray[np.float64] = np.array([0.012, 0.1, 0.97, 3.34, 35.2])
T_EXT: float = 300.0

# ---------------------------------------------------------------------
# Utilities
# ---------------------------------------------------------------------


def db_to_val(dB: float | NDArray[np.float64]) -> float | NDArray[np.float64]:
    """Convert dB (power) to linear factor."""
    return 10.0 ** (dB / 10.0)


# ---------------------------------------------------------------------
# Cabling model
# ---------------------------------------------------------------------

LineType = Literal["drive", "pump", "ffl", "dc"]


@dataclass
class Line:
    """
    Represents one physical line from MXC up to 50K/RT.

    Stage ordering is assumed to match STAGE_TEMPS:
        ['MXC', '100mK', 'Still', '4K', '50K']  (cold → warm)

    Attributes
    ----------
    type : LineType
        Logical type of line (drive/pump/ffl/dc); determines stage attenuators
        and cable technologies per span.
    technology : list[str]
        Cable technology per stage span; length matches number of stages.
    lengths : np.ndarray
        Per-span lengths (m), ordered cold → warm.
    """

    type: LineType = "drive"

    # Cable technologies per span (MXC→100mK→Still→4K→50K)
    technology: List[str] = field(default_factory=list)
    lengths: NDArray[np.float64] = field(default_factory=lambda: np.flipud(
        np.array([219.29e-3, 304.04e-3, 248.64e-3, 161.58e-3, 165.88e-3])
    ))
    fixed_pads_db: Optional[NDArray[np.float64]] = None

    cable_db_keys: Dict[str, str] = field(default_factory=lambda: {
    "coax": "SC-86/50-SCN-CN",
    "dc_PhBr": "PhBr_36AWG",
    "dc_NbTi": "NbTi_36AWG",
    })

    def __post_init__(self) -> None:
        if not self.technology:
            if self.type == "dc":
                self.technology = ["dc_NbTi", "dc_NbTi", "dc_NbTi", "dc_PhBr", "dc_PhBr"]
            else:
                self.technology = ["dense", "dense", "dense", "coax", "coax"]

    @staticmethod
    def _pure_cable_attenuation_per_m(tech: str) -> float:
        """
        Return attenuation per meter (dB/m) for a cable technology.
        """
        if tech == "dense":
            # 1.5 dB/m (https://delft-circuits.com/cri-oflex-technology/core-technology-cryogenic-rf-cable/#stripline-thermal)
            return 1.5
        if tech == "coax":
            # 3.2 dB/m at 4 K (https://www.coax.co.jp/en/product/sc/086-50-scn-cn.html)
            return 3.2
        if tech in {"dc_PhBr", "dc_NbTi"}:
            # DC lines assumed to have negligible RF attenuation here.
            return 0.0
        raise ValueError(f"Unsupported cable technology: {tech!r}")

    @property
    def attenuation(self) -> NDArray[np.float64]:
        # Choose base pads: override if provided, else defaults by type
        if self.fixed_pads_db is not None:
            base = self.fixed_pads_db
        else:
            if self.type == "drive":
                base = np.array([26.0, 20.0, 10.0, 10.0, 0.0])
            elif self.type == "pump":
                base = np.array([0.0, 15.0, 13.0, 15.0, 0.0])
            elif self.type == "ffl":
                base = np.array([0.0, 0.0, 0.0, 10.0, 6.0])
            elif self.type == "dc":
                base = np.zeros(5)
            else:
                raise ValueError(f"Unsupported line type: {self.type!r}")

        cable_db = np.array([self._pure_cable_attenuation_per_m(t) for t in self.technology]) * self.lengths
        return base + cable_db

    @property
    def cumulative_attenuation(self) -> NDArray[np.float64]:
        """Cumulative attenuation up the stack (dB)."""
        return np.cumsum(self.attenuation)

    # --------------------- thermal model ---------------------

    def _get_conductance_func(self, tech: str) -> Callable[[float], float]:
        """
        Return G_eff(T) [W / (m·K)] for a given cable technology.
        """
        if tech == "coax":
            key = self.cable_db_keys.get("coax", "SC-86/50-SCN-CN")
            cable = cables.db[key]
            return lambda T: (
                cable.outer.material.k(T) * cable.outer_area
                + cable.dielectric.material.k(T) * cable.dielectric_area
                + cable.inner.material.k(T) * cable.inner_area
            )
        if tech == "dense":
            A = 1.3e-9
            return lambda T: A * 4.6 * T**0.56
        if tech == "dc_PhBr":
            key = self.cable_db_keys.get("dc_PhBr", "PhBr_36AWG")
            cable = cables.db[key]
            return lambda T: cable.inner.material.k(T) * cable.inner_area
        if tech == "dc_NbTi":
            key = self.cable_db_keys.get("dc_NbTi", "NbTi_36AWG")
            cable = cables.db[key]
            return lambda T: cable.inner.material.k(T) * cable.inner_area

        raise ValueError(f"Unsupported cable technology: {tech!r}")

    def conductance(self, T: float) -> float:
        """
        Effective thermal conductance per unit length G_eff(T) [W/(m·K)],
        using the span that sandwiches temperature T.
        """
        if T < float(np.min(STAGE_TEMPS)):
            raise ValueError("Query temperature must be ≥ MXC temperature.")

        # Determine span index (clamped to last span when T is above top stage)
        idx = int(np.searchsorted(STAGE_TEMPS, T, side="right") - 1)
        idx = max(0, min(idx, len(STAGE_TEMPS) - 1))
        tech = self.technology[idx]
        return self._get_conductance_func(tech)(T)

    def passive_heat_load(self) -> List[float]:
        """
        Passive heat per stage span [W/m], computed as:
            Q_span = ∫ G_eff(T) dT over the span
            Return Q_span / length_span (per unit length).
        """
        loads: List[float] = []
        for i, tech in enumerate(self.technology):
            T_lo = float(STAGE_TEMPS[i])
            T_hi = float(STAGE_TEMPS[i + 1]) if i < len(self.technology) - 1 else T_EXT
            G = self._get_conductance_func(tech)
            Q, _ = quad(G, T_lo, T_hi)
            loads.append(Q / self.lengths[i])
        return loads


@dataclass
class Cabling:
    stage_names: List[str] = field(default_factory=lambda: ["MXC", "100mK", "Still", "4K", "50K"])
    # Carnot factor per stage
    carnot_factors: NDArray[np.float64] = field(
        default_factory=lambda: (T_EXT - STAGE_TEMPS) / STAGE_TEMPS
    )
    lines: Dict[LineType, Line] = field(
        default_factory=lambda: {
            "drive": Line(type="drive"),
            "pump": Line(type="pump"),
            "ffl": Line(type="ffl"),
            "dc": Line(type="dc"),
        }
    )

    def Atilde(self, stage: str = "MXC", line_type: LineType = "drive") -> float:
        """
        Cumulative attenuation at a given stage (dB).
        """
        if stage not in self.stage_names:
            raise ValueError(f"Unknown stage name: {stage!r}")
        stage_index = self.stage_names.index(stage)
        return float(self.lines[line_type].cumulative_attenuation[stage_index])

    def attenuation_factors(self, line_type: LineType = "drive") -> NDArray[np.float64]:
        """
        Linear attenuation factors per span, cold → warm. For span i:
            factor[i] = lin(A_cum[i]) - lin(A_cum[i-1]),
        with factor[0] = lin(A_cum[0]).
        """
        A_cum = self.lines[line_type].cumulative_attenuation
        lin = db_to_val(A_cum)
        out = np.empty_like(lin)
        out[0] = lin[0]
        out[1:] = lin[1:] - lin[:-1]
        return out

    def M_prefactor(self, line_type: LineType = "drive") -> float:
        """
        Total 'macro' prefactor that weights chip power by the Carnot factors
        and per-stage linear attenuation contributions.
        """
        return float(np.sum(self.carnot_factors * self.attenuation_factors(line_type=line_type)))


