@dataclass
class Hardware:
    # Dissipation/linewidths (angular units)
    k_1: float = 6.97 * kHz
    k_b: float = 24 * MHz
    k_phi: float = 0.08 * MHz
    k_ext: float = 400 * Hz

    # Circuit parameters
    M: float = 2 * pH  # inductive coupling (H)
    Z: float = 50.0  # Ohm
    E_J: float = 27 * GHz * 1e-34  # J = Hz * hbar (J·s / rad)
    E_L: float = 40 * GHz * 1e-34  # J = Hz * hbar (J·s / rad)
    omega_a: float = 4.29 * GHz
    omega_b: float = 7.21 * GHz
    Phi_0: float = 2e-15  # Weber

    # Participation / lever arms
    vphi_a: float = 0.14
    vphi_b: float = 0.19
    vphi_C: float = 0.10
    vphi_T: float = 0.20

    # Thermal occupancies
    ntha: float = 0.02
    nthb: float = 0.02

    # Measurement efficiency
    eta: float = 0.4

    # Cabling
    cabling: Cabling = field(default_factory=Cabling)

    # --------------------- helpers / derived ---------------------

    def get_alpha(self, T_bf: float) -> float:
        """
        Infer alpha from a target bit-flip time T_bf using a 1D root search.
        """
        gamma_bf = 1.0 / T_bf

        def Gamma_bf(alpha: float) -> float:
            # Units are 2π rad (consistent with k_1)
            return gamma_bf - self.k_1 * alpha ** 2 * np.exp(-4 * alpha ** 2) - self.k_1 * self.ntha * np.exp(
                -2 * alpha ** 2
            )

        # Search across a grid and root-find when sign flips.
        grid = np.linspace(0.0, 10.0, 200)
        vals = Gamma_bf(grid)
        sgn = np.sign(vals)
        root = 0.0
        for i in range(len(grid) - 1):
            if sgn[i] == 0:
                root = float(grid[i])
                break
            if sgn[i] + sgn[i + 1] == 0:
                try:
                    cand = scopt.brentq(Gamma_bf, float(grid[i]), float(grid[i + 1]))
                except ValueError:
                    continue
                if np.isfinite(cand):
                    root = float(cand)
                    break
        self.alpha = root
        return root

    # Macro prefactors from cabling
    @property
    def Mp(self) -> float:
        return self.cabling.M_prefactor(line_type="pump")

    @property
    def Md(self) -> float:
        return self.cabling.M_prefactor(line_type="drive")

    @property
    def Mz(self) -> float:
        # Zeno uses drive lines in your original code
        return self.cabling.M_prefactor(line_type="drive")

    # Shared ATS-cancellation coefficient
    @property
    def generic_ATS_cancellation_coef(self) -> float:
        return self.Z * (self.Phi_0 / (2 * np.pi * self.M)) ** 2 * (1 + 4 * self.E_J ** 2 / self.E_L ** 2)

    def g_to_eps_coef(self, interaction: Literal["stab", "CNOT", "longitudinal"] = "stab") -> float:
        if interaction == "CNOT":
            return HBAR / (self.E_J * self.vphi_C * self.vphi_T ** 2)
        if interaction == "stab":
            return 2 * HBAR / (self.E_J * self.vphi_b * self.vphi_a ** 2)
        if interaction == "longitudinal":
            return HBAR / (self.E_J * self.vphi_b * self.vphi_a ** 2)
        raise ValueError(f"Unsupported interaction type: {interaction!r}")

    def ATS_cancellation_coef(self, interaction: Literal["stab", "CNOT", "longitudinal"] = "stab") -> float:
        return self.g_to_eps_coef(interaction) ** 2 * self.generic_ATS_cancellation_coef

    # Shorthand properties
    @property
    def p(self) -> float:
        return self.ATS_cancellation_coef(interaction="stab")

    @property
    def c(self) -> float:
        return self.ATS_cancellation_coef(interaction="CNOT")

    @property
    def l(self) -> float:
        return self.ATS_cancellation_coef(interaction="longitudinal")

    @property
    def d(self) -> float:
        return HBAR * self.omega_b / self.k_b

    @property
    def z(self) -> float:
        return HBAR * self.omega_a / self.k_ext

    # Bit-flip time model
    def T_bf(self, alpha: float) -> float:
        rate = self.k_1 * alpha ** 2 * np.exp(-4 * alpha ** 2) + self.k_1 * self.ntha * np.exp(-2 * alpha ** 2)
        return 1.0 / rate

    def p_z(self, alpha: float, Tgate: float) -> float:
        return alpha ** 2 * self.k_1 * Tgate * (1 + 2 * self.ntha)